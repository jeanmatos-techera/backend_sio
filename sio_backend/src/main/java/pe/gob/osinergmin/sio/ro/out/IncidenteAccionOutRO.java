package pe.gob.osinergmin.sio.ro.out;

public class IncidenteAccionOutRO {
	private Integer idEstadoIncidente;
	private String estadoIncidente;
	private String estado;
	
	public Integer getIdEstadoIncidente() {
		return idEstadoIncidente;
	}
	public void setIdEstadoIncidente(Integer idEstadoIncidente) {
		this.idEstadoIncidente = idEstadoIncidente;
	}
	public String getEstadoIncidente() {
		return estadoIncidente;
	}
	public void setEstadoIncidente(String estadoIncidente) {
		this.estadoIncidente = estadoIncidente;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	
}
